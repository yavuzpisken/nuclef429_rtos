void uart_string_transmit(const char * format, ...) {
	va_list args;
	uint8_t buffer[100] = { 0 };

	memset(buffer, 0, 100);

	va_start(args, format);
	vsnprintf((char*) buffer, 100, format, args);
	va_end(args);

	HAL_UART_Transmit(&huart2, buffer, 100, 1000);
}


